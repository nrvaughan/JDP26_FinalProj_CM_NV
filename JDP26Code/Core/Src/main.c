void HAL_ADC_ConvHalfCpltCallback(ADC_HandleTypeDef* hadc) // This callback is called when the ADC DMA has filled the first half of the buffer, since we use two channels it leaves time to get both aux + mic samples before mixing
{
  if (hadc->Instance == ADC1)
  {
    halfCount++; // debugging
    adcHalfReady = 1;
  }
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc) // Same as above, but the second half
{
  if (hadc->Instance == ADC1)
  {
    fullCount++; // debugging
    adcFullReady = 1;
  }
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) // The main audio processing happens in this timer function
{
  if (htim->Instance == TIM6) // 44.1kHz timer set up in CubeMX
  {

    if (HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_5) == GPIO_PIN_RESET) //knife switch, if closed, software mute by outputting DC offset only
    {
  // Knife switch active: mute audio in software
      HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, 2048);
      return;
    }

    uint32_t dmaRemaining = __HAL_DMA_GET_COUNTER(hadc1.DMA_Handle); // Determine where in ADC buffer we are writing at, to not overwrite unread samples
    uint32_t writeIndex = ADC_BUF_LEN - dmaRemaining; // Tells us the index of the most recent sample
  
    if (writeIndex >= ADC_BUF_LEN) // Once it hits 512, it wraps back to 0
    {
      writeIndex = 0;
    }

    uint32_t readIndex; // We want to read the sample that was just fully written, which is 2 samples (mic + aux) behind the current write index

    if (writeIndex >= 2) 
    {
      readIndex = writeIndex - 2; // Read the sample that was just fully written
    }
    else
    {
      readIndex = ADC_BUF_LEN - 2;
    }

    // Ensure readIndex points to mic sample, even index
    if (readIndex % 2 != 0)
    {
      readIndex--; // ALWAYS starts with mic sample, just in case
    }

    uint16_t micRaw = adcBuffer[readIndex];       // PA0
    uint16_t auxRaw = adcBuffer[readIndex + 1];   // PA1

    int32_t micAC = (int32_t)micRaw - 2056; // removing DC offsets
    int32_t auxAC = (int32_t)auxRaw - 2033; 

    // mic + aux gain
    int32_t micScaled = micAC;
    int32_t auxScaled = auxAC / 4;

    int32_t mixedAC = micScaled + auxScaled;
    int32_t dacOut = 2048 + mixedAC; // adding back DC offset for output

    if (dacOut < 0) dacOut = 0;
    if (dacOut > 4095) dacOut = 4095; // clipping

    HAL_DAC_SetValue(&hdac1, DAC_CHANNEL_1, DAC_ALIGN_12B_R, (uint32_t)dacOut); // DAC Output on PA4
  }
}

