#define ADC_BUF_LEN 512
#define ADC_MID     2048

uint16_t adcBuffer[ADC_BUF_LEN];
volatile uint8_t adcHalfReady = 0;
volatile uint8_t adcFullReady = 0;
volatile uint32_t halfCount = 0;
volatile uint32_t fullCount = 0;

int pwm1Dutys[16] = {39, 44, 50, 59, 71, 88, 112, 147, 195, 270, 372, 525, 750, 1100, 1625, 2475};
int pwm2Dutys[16] = {55, 60, 67, 76, 90, 108, 134, 172, 225, 302, 417, 585, 830, 1200, 1749, 2630};

int freqInd = 7;

/* USER CODE END PV */

