int main(void)
{

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();


  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_DAC1_Init();
  MX_TIM6_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

    /* Encoder/PWMs */
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);  // PB4, PWM1
  __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, 2475);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);  // PB10, PWM2
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, 2630);

  /* ADC + DAC Startup */
  HAL_DAC_Start(&hdac1, DAC_CHANNEL_1);
  HAL_ADCEx_Calibration_Start(&hadc1, ADC_SINGLE_ENDED);
  HAL_ADC_Start_DMA(&hadc1, (uint32_t*)adcBuffer, ADC_BUF_LEN);
  HAL_TIM_Base_Start_IT(&htim6);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

while (1) /* this loop handles chaning PWM via rotary encoder */
{
  static uint8_t lastA = 0;
  static uint8_t initialized = 0;
  static uint32_t lastStepTime = 0; // all for determining encoder state and debouncing

  // Read encoder pins, active high with pull-downs, so SET means 1, RESET means 0
  uint8_t A = (HAL_GPIO_ReadPin(GPIOC, GPIO_PIN_7) == GPIO_PIN_SET) ? 1 : 0;
  uint8_t B = (HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_6) == GPIO_PIN_SET) ? 1 : 0;

  if (!initialized)
  {
    lastA = A; // initialize lastA to current state to avoid false step detection on startup
    initialized = 1;
  }

  if ((HAL_GetTick() - lastStepTime) > 50) // debounce
  {
    if ((lastA == 0) && (A == 1)) // detect rising edge on A
    {
      lastStepTime = HAL_GetTick();

      if (B == 0) // determines CW vs CCW based on state of B when A rises
      {
        if (freqInd < 15) freqInd++; //increase
      }
      else
      {
        if (freqInd > 0) freqInd--; //decrease
      }

      __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, pwm1Dutys[freqInd]);
      __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, pwm2Dutys[freqInd]); // update PWM duty cycles based on new index
    }
  }

  lastA = A;
}

    /* USER CODE END WHILE */

}


